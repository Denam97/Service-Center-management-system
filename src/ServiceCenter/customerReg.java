    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ResultSet rs = v.getTable("SELECT `name`, `telephone`, `vehicleNo`, `vehicleBrand`, `Model`, `Frequency` FROM `customer` WHERE telephone='" + tel.getText() + "'");
        try {
            if (!rs.next()) {
                JOptionPane.showMessageDialog(null, "Customer not found");
                cus.setText("");
            } else {
                rs.beforeFirst();
                while (rs.next()) {
                    no.setText(rs.getString("vehicleNo"));
                    cus.setText(rs.getString("name"));
                    brand.setText(rs.getString("vehicleBrand"));
                    name.setText(rs.getString("Model"));
                    vtype.setText(rs.getString("Frequency"));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(customerReg.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

