    private void uvehActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uvehActionPerformed
        if (uservice.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select a service!");
        } else {
            try {
                if (!uveh.getSelectedItem().toString().equals("") && !uservice.getSelectedItem().toString().equals("")) {
                    ResultSet rs = v.getTable("select service.cost,service.ServiceID from service where VehicleType='" + uveh.getSelectedItem().toString() + "' AND ServiceType='" + uservice.getSelectedItem().toString() + "'");

                    while (rs.next()) {
                        if (rs.getString(1).equals("")) {
                            uup.setText("");
                            usid.setText("");
                        } else {
                            uup.setText(rs.getString(1));
                            usid.setText(rs.getString(2));
                        }
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(customerReg.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_uvehActionPerformed

